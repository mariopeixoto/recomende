package br.ic.grow.retriblog.data;

public class WeblogAuthor {

	String username, firstname, lastname, description, bio, thumbnailpicture;

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getFirstname() {
		return firstname;
	}

	public void setFirstname(String firstname) {
		this.firstname = firstname;
	}

	public String getLastname() {
		return lastname;
	}

	public void setLastname(String lastname) {
		this.lastname = lastname;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getBio() {
		return bio;
	}

	public void setBio(String bio) {
		this.bio = bio;
	}

	public String getThumbnailpicture() {
		return thumbnailpicture;
	}

	public void setThumbnailpicture(String thumbnailpicture) {
		this.thumbnailpicture = thumbnailpicture;
	}
	
	
}
